# 3. Створити список із цілих чисел. 
# Його необхідно відфільтрувати, щоб у результаті 
# залишилися парні числа. Вивести квадрати цих чисел,
# використовуючи map.

l1 = [1,76,3,90,5,148,45]
l2 = list()
for i in l1:
    if i %2==0:
        l2.append(i)
    else:
        continue
squares = list(map(lambda x: x**2, l2))
print("Парні значення: ", l2)
print("Квадрати: ", squares)